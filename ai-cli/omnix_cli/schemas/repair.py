"""Schemas for the Repair Agent and Repair Loop."""

from __future__ import annotations

from datetime import datetime
from enum import StrEnum

from pydantic import BaseModel, ConfigDict, Field


class RepairSeverity(StrEnum):
    """Severity level of a repair action — mirrors QA severity."""

    CRITICAL = "critical"
    HIGH = "high"
    MEDIUM = "medium"
    LOW = "low"


class RepairStatus(StrEnum):
    """Lifecycle state of a single repair plan item."""

    PENDING = "pending"
    PLANNED = "planned"
    IN_PROGRESS = "in_progress"
    COMPLETED = "completed"
    FAILED = "failed"


class RepairPlanItem(BaseModel):
    """A single structured repair action derived from a QA finding."""

    model_config = ConfigDict(extra="forbid")

    id: str
    severity: RepairSeverity
    issue: str
    strategy: str
    target_agent: str  # e.g. "backend", "frontend", "database", "routing"
    qa_finding_id: str  # reference to the originating QA finding
    status: RepairStatus = RepairStatus.PENDING


class RepairPlan(BaseModel):
    """Ordered collection of repair actions for one repair cycle."""

    model_config = ConfigDict(extra="forbid")

    project_name: str
    cycle: int
    timestamp: datetime = Field(default_factory=datetime.now)
    items: list[RepairPlanItem] = Field(default_factory=list)

    @property
    def critical_count(self) -> int:
        return sum(1 for i in self.items if i.severity == RepairSeverity.CRITICAL)

    @property
    def high_count(self) -> int:
        return sum(1 for i in self.items if i.severity == RepairSeverity.HIGH)

    @property
    def medium_count(self) -> int:
        return sum(1 for i in self.items if i.severity == RepairSeverity.MEDIUM)

    @property
    def low_count(self) -> int:
        return sum(1 for i in self.items if i.severity == RepairSeverity.LOW)


class RepairArtifact(BaseModel):
    """An artifact generated by the Repair Agent to address one or more findings."""

    model_config = ConfigDict(extra="forbid")

    id: str
    repair_plan_id: str  # references RepairPlanItem.id
    qa_finding_id: str   # references the originating QA finding
    target_agent: str
    title: str
    description: str = ""
    content: str
    generated_at: datetime = Field(default_factory=datetime.now)
    cycle: int
    version: int = 1


class RepairReport(BaseModel):
    """Summary report for one repair cycle."""

    model_config = ConfigDict(extra="forbid")

    project_name: str
    cycle: int
    timestamp: datetime = Field(default_factory=datetime.now)
    issues_processed: int
    critical_count: int
    high_count: int
    medium_count: int
    low_count: int
    artifacts_generated: int
    plan_item_ids: list[str] = Field(default_factory=list)
    repair_artifact_ids: list[str] = Field(default_factory=list)
    expected_impact: str = ""
    status: str = "COMPLETE"


class RepairCycleEntry(BaseModel):
    """A single entry in the repair cycle history — one per repair run."""

    model_config = ConfigDict(extra="forbid")

    cycle: int
    timestamp: datetime = Field(default_factory=datetime.now)
    issues_addressed: int
    artifacts_generated: int
    quality_score_before: int
    status: str


class RepairHistory(BaseModel):
    """Complete audit log of all repair cycles for a project."""

    model_config = ConfigDict(extra="forbid")

    project_name: str
    cycles: list[RepairCycleEntry] = Field(default_factory=list)

    def get_next_cycle(self) -> int:
        if not self.cycles:
            return 1
        return max(c.cycle for c in self.cycles) + 1
